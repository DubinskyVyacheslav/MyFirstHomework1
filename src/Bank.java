public class Bank {


    public String name;
    public int age;
    public float money;
    public long id;

    public Bank(String name,int age ,float money, long id) {
        this.name = name;
        this.age = age;
        this.money = money;
        this.id = id;
    }


    public int getAge() {
        return age;
    }

    public float getMoney() {
        return money;
    }

    public long getId() {
        return id;
    }
    public String getname() {
        return name;
    }
}






